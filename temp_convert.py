# Description: Prints the equivalent temperature in Fahrenheit from Celsius

print("This program gives the temperature in Fahrenheit from Celsius")
temp_c = float(input("Please enter temperature in Celsius: "))

temp_f = temp_c * (9/5) + 32

print("The equivalent temperature in Fahrenheit is: ", temp_f)